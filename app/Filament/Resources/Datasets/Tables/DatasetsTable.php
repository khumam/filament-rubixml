<?php

namespace App\Filament\Resources\Datasets\Tables;

use App\Actions\Dataset\TrainDataset;
use App\Models\Dataset;
use Filament\Actions\Action;
use Filament\Actions\BulkActionGroup;
use Filament\Actions\DeleteBulkAction;
use Filament\Actions\EditAction;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Table;

class DatasetsTable
{
    public static function configure(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('name')
                    ->searchable(),
                TextColumn::make('label_column')
                    ->searchable(),
                TextColumn::make('exclude_column')
                    ->searchable(),
                TextColumn::make('algorithm')
                    ->searchable(),
                TextColumn::make('created_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
                TextColumn::make('updated_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
            ])
            ->filters([
                //
            ])
            ->recordActions([
                Action::make('train')
                    ->label('Train')
                    ->icon('heroicon-o-rocket-launch')
                    ->action(fn (Dataset $dataset) => TrainDataset::run($dataset)),
                EditAction::make(),
            ])
            ->toolbarActions([
                BulkActionGroup::make([
                    DeleteBulkAction::make(),
                ]),
            ]);
    }
}
